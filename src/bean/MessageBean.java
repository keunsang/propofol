package bean;

public class MessageBean {
	private String m_num;
	private String m_title;
	private String m_content;
	private String m_s_time;
	private String m_s_id;
	private String m_r_id;

	public String getM_num() {
		return m_num;
	}

	public void setM_num(String m_num) {
		this.m_num = m_num;
	}

	public String getM_title() {
		return m_title;
	}

	public void setM_title(String m_title) {
		this.m_title = m_title;
	}

	public String getM_content() {
		return m_content;
	}

	public void setM_content(String m_content) {
		this.m_content = m_content;
	}

	public String getM_s_time() {
		return m_s_time;
	}

	public void setM_s_time(String m_s_time) {
		this.m_s_time = m_s_time;
	}

	public String getM_s_id() {
		return m_s_id;
	}

	public void setM_s_id(String m_s_id) {
		this.m_s_id = m_s_id;
	}

	public String getM_r_id() {
		return m_r_id;
	}

	public void setM_r_id(String m_r_id) {
		this.m_r_id = m_r_id;
	}
}
